package week1.Day1;

public class Calculator {
	
	public int addition(int number1,int number2) {
		
		int a=number1;
		int b=number2;
		int c =a+b;
		return c;
		
	}
	public int subtract(int number1,int number2) {
		int a=number1;
		int b=number2;
		int c =a-b;
		return c;
		
	}
	public int multiply(int number1,int number2) {
		int a=number1;
		int b=number2;
		int c =a*b;
		return c;
				
	}
	public int division(int number1,int number2) {
		int a=number1;
		int b=number2;
		int c =a/b;
		return c;
	}

	
}
